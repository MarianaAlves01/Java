public class ConversaoUnidades 
{
	
    public static double metroQuadradoParaPeQuadrado(double a) 
    {
        return a * 10.76;
    }

    public static double peQuadradoParaCentimetroQuadrado(double b) 
    {
        return b * 929;
    }

    public static double milhaQuadradaParaAcre(double c) 
    {
        return c * 640;
    }

    public static double acreParaPeQuadrado(double d) 
    {
        return d * 43560;
    }
}
